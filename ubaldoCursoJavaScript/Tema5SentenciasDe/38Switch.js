let numero=3;
let numeroTexto="Valor desconocido";
switch(numero){
    case 1:
        numeroTexto="uno";
        break;
    case 2:
        numeroTexto="dos";
        break;
    case 3:
        numeroTexto="tres";
        break;
    case 4:
        numeroTexto="cuatro";
        break;
    default:
        numeroTexto="Numero desconocido";
}
console.log(numeroTexto);
